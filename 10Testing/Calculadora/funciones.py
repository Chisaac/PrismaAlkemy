'''
Minimal calculator created for testing

returns answer
rtype: float
'''
def add(number1,number2) -> int:
    addition=number1+number2
    return addition

def substract(number1,number2) -> int:
    substraction=number1-number2
    return substraction

def multiply(number1,number2) -> int:
    product=number1*number2
    return product

def divide(number1,number2) -> int:
    quotient=number1/number2
    return quotient